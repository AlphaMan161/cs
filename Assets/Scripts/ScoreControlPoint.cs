public class ScoreControlPoint
{
    private int score;

    private short team;

    public int Score
    {
        get
        {
            return this.score;
        }
    }

    public short Team
    {
        get
        {
            return this.team;
        }
    }

    public ScoreControlPoint(int Score, short Team)
    {
        this.score = Score;
        this.team = this.team;
    }

    public void Set(int Score, short Team)
    {
        this.score = Score;
        this.team = Team;
    }
}


